// the function checks half of the stack if it has been allocated starting from the starting address
uint8_t check_stack( StackType_t* stack, uint16_t stack_size )
{		
	for( uint16_t i = 0; i < (stack_size/2); i++ ) 
	{
		if( stack[i] != 0xA5A5A5A5UL ) return 0;
	}
	return 1;
}
	

// function is the idle task hook and it is called automatically=======================
void vApplicationIdleHook( void * pvParameters )
{
	 if( check_stack( xStack, STACK_SIZE ) == 0 )
	 {
		 // ERROR HALF STACK
		 HAL_GPIO_WritePin( LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET );
	 }
	 else
	 {		
		 // stack allocated correctly
		 HAL_GPIO_WritePin( LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET );
	 }
}

